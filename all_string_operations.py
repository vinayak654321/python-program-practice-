# File Name: all_string_operations.py
# Program: String Indexing, Slicing and Methods

s = "Python Programming"

# String Indexing
print("Indexing:")
print(s[0])     # First character
print(s[3])     # Fourth character
print(s[-1])    # Last character

# String Slicing
print("\nSlicing:")
print(s[0:6])   # Python
print(s[7:18])  # Programming
print(s[:6])    # Python
print(s[7:])    # Programming

# String Methods
print("\nString Methods:")
print(s.upper())                      # Uppercase
print(s.lower())                      # Lowercase
print(s.replace("Python", "Java"))    # Replace
